#include <stdio.h>

int main() {
    int taka = 50;

    printf("taka + 30 = %d\n", taka + 30);
    printf("taka - 20 = %d\n", taka - 20);
    printf("taka * 20 = %d\n", taka * 20);
    printf("taka / 20 = %d\n", taka / 20);
    printf("taka = %d\n", taka);


    int a = 20;
    int b = 30;

    printf("a+b = %d\n", a+b);
    printf("a-b = %d\n", a-b);
    printf("a*b = %d\n", a*b);
    printf("a/b = %d\n", a/b);
    

    int x = 50;
    int y = 30;

    printf(" %d + %d = %d\n", x,y, x+y);
    printf(" %d - %d = %d\n", x,y, x-y);
    printf(" %d * %d = %d\n", x,y, x*y);
    printf(" %d / %d = %d\n", x,y, x/y);

    return 0;
}